     * 基于Apriori算法的频繁项集挖掘的关联规则学习
     * @return \think\Response
     */
    public function demo()
    {
        $samples = [[1, 3], [1, 4], [2, 4], [3, 1], [4, 1], [4, 2]];
        $labels = ['a', 'a', 'a', 'b', 'b', 'b'];
        $classifier = new KNearestNeighbors();
        $classifier->train($samples,$labels);
        $res = $classifier->predict([3,2]);
        halt($res);
    }

